import com.oop.service.BidService;
import com.oop.service.impl.BidServiceImpl;

/**
 * Servlet implementation class UpdateBidServlet
 */
@WebServlet(urlPatterns="/UpdateBidServlet")
public class UpdateBidServlet extends HttpServlet {
	private static final long serialVersionUID = 1L;
       
    /**
     * @see HttpServlet#HttpServlet()
     */
    public UpdateBidServlet() {
    	
        super();
        // TODO Auto-generated constructor stub
        System.out.println("GGGGGGGGGGGGGGGGGGGGGGGg");
    }

	/**
	 * @see HttpServlet#doGet(HttpServletRequest request, HttpServletResponse response)
	 */
	protected void doGet(HttpServletRequest request, HttpServletResponse response) throws ServletException, IOException {
		// TODO Auto-generated method stub
		response.getWriter().append("Served at: ").append(request.getContextPath());
	}

	/**
	 * @see HttpServlet#doPost(HttpServletRequest request, HttpServletResponse response)
	 */
	protected void doPost(HttpServletRequest request, HttpServletResponse response) throws ServletException, IOException {
		
		
		String bidID = request.getParameter("bidID");
		String bidPrice	=request.getParameter("price");
		
	System.out.println("Tin tin tin");
		System.out.println(bidID);
		System.out.println(bidPrice);
		
		BidService bService = new BidServiceImpl();
		
		try {
		double startPrice = bService.getprice(bidID);
		System.out.println("Starting Priceeeeeeeeeeeee:"+startPrice);
		
		if(startPrice > Double.parseDouble(bidPrice)) {
			PrintWriter writer = response.getWriter();
			
			writer.println("<script>");
			writer.println("alert('Enter a price greater than initial product price')");
			writer.println("</script>");
		}
		else {
			
			boolean isUpdate = false;


			isUpdate = bService.UpdateBid(Integer.parseInt(bidID),Double.parseDouble(bidPrice));

			if(isUpdate == true) {
				
				PrintWriter writer = response.getWriter();
				
				writer.println("<script>");
				writer.println("alert('Updated Successfully')");
				writer.println("</script>");
				
			}
		}
			
		} catch (SQLException e) {
			// TODO Auto-generated catch block
			e.printStackTrace();
		}
		

		doGet(request, response);
	

}}





































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































